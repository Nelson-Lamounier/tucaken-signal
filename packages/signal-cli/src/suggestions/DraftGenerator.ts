function draftAdrs(s: Suggestion, ctx: DraftContext): DraftResult {
  const lines: string[] = [];
  ctx.decisions.forEach((d, i) => {
    const n = String(i + 1).padStart(4, "0");
    lines.push(`## ${n}-${slug(d.topic)}.md`);
    lines.push("");
    lines.push(`# ${n}. ${d.topic}: chose ${d.chose}`);
    lines.push("");
    lines.push(`**Status:** accepted`);
    lines.push("");
    lines.push(`## Context`);
    lines.push(`<!-- Why was this decision needed? What constraints applied? -->`);
    lines.push("");
    lines.push(`## Decision`);
    lines.push(`Chose **${d.chose}** over ${d.over.join(", ")}.`);
    lines.push("");
    lines.push(`## Consequences`);
    lines.push(`<!-- What does this make easier? Harder? What did you give up? -->`);
    lines.push("");
    lines.push(`*Evidence:* \`${d.evidencePath}\``);
    lines.push("");
    lines.push("---");
    lines.push("");
  });
  return {
    suggestionId: s.id,
    filename: "docs/adr/ADRS-DRAFT.md",
    content: `# ADR drafts (split into separate files)\n\n${lines.join("\n")}`,
  };
}

