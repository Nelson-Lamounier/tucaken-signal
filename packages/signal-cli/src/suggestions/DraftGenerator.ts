import type { TrustSignalReport, Suggestion } from "../types.js";
import type { DetectedDecision } from "../analyzers/system_thinking/DecisionDetector.js";
import type { LlmClient } from "../llm/LlmClient.js";

export interface DraftContext {
  report: TrustSignalReport;
  diagramDraft: string | null;
  decisions: DetectedDecision[];
  llm?: LlmClient;
}

export async function enhanceWithLlm(
  draft: DraftResult,
  suggestion: Suggestion,
  ctx: DraftContext
): Promise<DraftResult> {
  if (!ctx.llm || !ctx.llm.available) return draft;
  const sys =
    "You are improving an engineer's portfolio documentation. Keep the user's edits intact " +
    "(anything between <!-- comments -->). Return ONLY the improved markdown.";
  const user = `Suggestion: ${suggestion.title}\n\nDraft to improve:\n\n${draft.content}`;
  try {
    const enhanced = await ctx.llm.complete({ systemPrompt: sys, userPrompt: user, maxTokens: 2000 });
    if (enhanced && enhanced !== "__IDE_BRIDGE__") return { ...draft, content: enhanced };
  } catch {/* fall through to original */}
  return draft;
}

export interface DraftResult {
  suggestionId: string;
  filename: string;
  content: string;
}

export function generateDraft(suggestion: Suggestion, ctx: DraftContext): DraftResult | null {
  const id = suggestion.id.split(".").pop()!;

  if (id === "ai_usage_doc" || id === "ai_transparency_section") return draftAiUsage(suggestion);
  if (id === "architecture_diagram_missing" || id === "no_architecture_diagram") {
    // Always draftable — fall back to a Mermaid stub when no diagram was
    // auto-derived, so the scan→apply handoff never dead-ends.
    const diagram = ctx.diagramDraft ?? STUB_DIAGRAM;
    return { suggestionId: suggestion.id, filename: "docs/architecture.md", content: archDoc(diagram) };
  }
  if (id === "adr_for_decisions" || id === "adr_backfill" || id === "adr_starter") return draftAdrs(suggestion, ctx);
  if (id === "deployment_proof") return draftDeploymentProof(suggestion);
  if (id === "postmortem_invitation") return draftPostmortem(suggestion);
  if (id === "add_usage_example") return draftUsageExample(suggestion);
  return null;
}

